// Update positions for all alive sprites.
void Move(void){ 
	uint8_t num_life = 0;
  uint8_t i;
	//shoot
	if(SW1_pressed){
		Laser.x = PlayerShip.x + PLAYERW/2;
		Laser.y = PlayerShip.y - PLAYERH + 1;
		Laser.life = 1;
		SW1_pressed = 0;
	}
	
	// Move Laser
  if(Laser.y > MAX_LASERY){
    Laser.y -= 2;
  }else{
		Laser.life = 0;
	}
	for(i=0;i<3;i++){
		num_life += 1;
		if(Enemy[i].x < MAX_ENEMYX){
			Enemy[i].x += 1;
				if(Enemy[i].x % 2 == 1){//controls flapping
										Enemy[i].image = SmallEnemyPointB[i];
						}
						else if(Enemy[i].x % 2 == 0){
										Enemy[i].image = SmallEnemyPointA[i];
						}
		}
		else if (Enemy[i].image == SmallExplosion0)
		{
			Enemy[i].life = 0;
		}
		else{
			Enemy[i].life = 0;
			num_life -= 1;
		}
		if((Laser.x+LASERW >= Enemy[i].x) && (Laser.x <= Enemy[i].x+ENEMYW-4) && (Laser.y <= Enemy[i].y+ENEMYH- 4) && (Laser.life == 1) && (Enemy[i].life)){
			Enemy[i].life = 1;
			score += 1;
			Enemy[i].image = SmallExplosion0;
			Sound_Explosion();
			Laser.life = 0;
		}
	}
	PlayerShip.x = (Convert(ADC_In())*55) >> 10;
	if (num_life==0) {
		game_s = OVER;
	}
}

